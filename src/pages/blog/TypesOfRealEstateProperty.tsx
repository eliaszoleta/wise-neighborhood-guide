    <p>
      Raw land is the simplest concept and the trickiest investment. It produces no rental income. It doesn't depreciate for tax purposes (unlike improved property). Banks actively dislike lending on it — expect 20–50% down and higher rates when you can get a loan at all. And it can sit unproductive for years while you pay property taxes and wait for the right buyer or development trigger.
    </p>
